# less and greater < & >

puts 1 > 2
puts 1 < 2

# less than or equal <=; greater than or equal >=

puts 5 >= 5
puts 5 <= 4

# equal == & not equal !=

puts 1 == 1
puts 2 != 1

# comparing strings = comparing their lexicographical ordering

puts 'cat' < 'dog'
puts ''

puts 'bug lady' < 'Xander'
puts 'bug lady'.downcase < 'Xander'.downcase
puts ''

puts 2 < 10
puts '2' < '10'
